CREATE TABLE TB_GROUP_REVIEW(
    REVIEW_NO NUMBER,
    GROUP_NO NUMBER,
    MEMBER_NO NUMBER,
    GROUP_SCORE NUMBER,
    REVIEW VARCHAR2(4000),
    FOOT_PRINT VARCHAR2(5),
    PENALTY NUMBER,
    CONSTRAINT PK_TB_REVIEW_NO PRIMARY KEY(REVIEW_NO),
    CONSTRAINT FK_TB_GROUP_NO FOREIGN KEY(GROUP_NO),
    CONSTRAINT FK_TB_MEMBER_NO FOREIGN KEY(MEMBER_NO)
);

CREATE TABLE TB_PER_REIVEW(
    REVIEW_NO NUMBER,
    MEMBER_NO NUMBER,
    PENALTY NUMBER,
    FOOT_PRINT NUMBER,
    CONSTRAINT PK_TB_REVIEW_NO PRIMARY KEY(REVIEW_NO),
    CONSTRAINT FK_TB_MEMBER_NO FOREIGN KEY(MEMBER_NO)
);